/*
 Problem Statement:
    Write a program to find the area of a rectangle.
    */
/*
    Time Complexity: O(1)
    Space Complexity: O(1)
    
 */

package oops;

public class AreaOfRectangle {
    /*
     * Time Complexity: O(1)
     * Space Complexity: O(1)
     */
    int length;
    int breadth;

    public int getArea() {
        int area = length * breadth;
        return area;
    }
}